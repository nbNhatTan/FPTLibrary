public class WishListDTO {
    private int bookID;
    private String userID;

    public WishListDTO(int bookID, String userID) {
        this.bookID = bookID;
        this.userID = userID;
    }

    public int getBookID() {
        return bookID;
    }

    public void setBookID(int bookID) {
        this.bookID = bookID;
    }

    public String getUserID() {
        return userID;
    }

    public void setUserID(String userID) {
        this.userID = userID;
    }


}
